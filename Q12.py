# Jack bought 200 burgers for the school picnic. They were contained in packages of 8 burgers.
# Write a program in python to find how many total packages did he buy?

total_burgers = 200
print("Total burgers Jack bought = ", total_burgers)
each_package_contains = 8
print("Each Package contains = ", each_package_contains, "Burgers")

total_packages = total_burgers / each_package_contains
print("The total packages Jack buys = ", total_packages, "Packages")